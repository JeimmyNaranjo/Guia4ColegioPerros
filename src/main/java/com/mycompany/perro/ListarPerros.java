private void mostrarPerros (){
        try {
       List<Perro> perros = tablaPerros.queryForAll();
      
        listap.setText(String.format("%10s  %-30s  %-30s  %-30s  %-30s  %-20s  %-20s\n",
                "Nombre Perro", "   Raza", "Localidad", "Cedula Due単o", "Nombre Due単o", "Telefono", "Dia Asistencia"));
        
        
        for (Perro i : perros){
            listap.append(String.format("%-20s  %-20s  %-40s  %-30s  %-30s  %-30s  %-30s\n",
                    i.getNombre(),
                    i.getRaza(),
                    i.getLocalidad(),
                    i.getCeduladue単o(),
                    i.getNombreDue単o(),
                    i.getTelefono(),
                    i.getDiaAsistencia()));
    }                                    
    }
        catch (SQLException ex){
            Logger.getLogger(ListarPerros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

