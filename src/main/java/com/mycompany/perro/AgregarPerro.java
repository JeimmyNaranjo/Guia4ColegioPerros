     private boolean existePerro(int cedula)throws SQLException{
           Perro i = tablaPerros.queryForId(cedula);
           if (i != null){
               return true;
           }
           return false;
        }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombrePerro = nombreP.getText().toUpperCase();
        String Raza = raza.getText().toUpperCase();
        String local = localidad.getText().toUpperCase();
        int Cedula = Integer.parseInt(cedula.getText().trim());
        String nombreDue単o = nombreD.getText().toUpperCase();
        int Telefono = Integer.parseInt(tel.getText().trim());
        String DiaAsistencia = asistencia.getText().toUpperCase();
        
        Perro can = new Perro(nombrePerro, Raza, local, Cedula, nombreDue単o, Telefono, DiaAsistencia);
        
        try{
            if (existePerro(Cedula)){
                JOptionPane.showMessageDialog(null, "Ya existe un due単o con la cedula" + cedula);
            }
            else{
               tablaPerros.create(can);
              JOptionPane.showMessageDialog(null," Perro agregado correctamente");
            }
            }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


